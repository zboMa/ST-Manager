export const RUNTIME_CHANNEL = 'st-manager:render-runtime';

export function buildViewportSyncScript(runtimeId, initialViewportHeight = window.innerHeight || 0) {
    return [
        '(function () {',
        `  const CHANNEL = ${JSON.stringify(RUNTIME_CHANNEL)};`,
        `  const RUNTIME_ID = ${JSON.stringify(runtimeId)};`,
        '  const root = document.documentElement;',
        '  function applyViewportHeight(height) {',
        '    if (!Number.isFinite(height) || height <= 0) return;',
        "    root.style.setProperty('--stm-viewport-height', `${height}px`);",
        '  }',
        `  applyViewportHeight(${JSON.stringify(Number(initialViewportHeight) || 0)});`,
        "  window.addEventListener('message', function (event) {",
        '    const data = event && event.data ? event.data : null;',
        '    if (!data || data.channel !== CHANNEL || data.runtimeId !== RUNTIME_ID || data.type !== "viewport") return;',
        '    applyViewportHeight(Number(data.height));',
        '  });',
        '})();',
    ].join('\n');
}

export function buildAutoHeightScript(runtimeId) {
    return [
        '(function () {',
        `  const CHANNEL = ${JSON.stringify(RUNTIME_CHANNEL)};`,
        `  const RUNTIME_ID = ${JSON.stringify(runtimeId)};`,
        '  let scheduled = false;',
        '  function measureIntrinsicHeight() {',
        '    const body = document.body;',
        '    if (!body) return 0;',
        '    let contentHeight = 0;',
        '    const bodyRect = body.getBoundingClientRect();',
        '    const elements = [body, ...Array.from(body.querySelectorAll("*"))];',
        '    elements.forEach((element) => {',
        '      if (!(element instanceof Element)) return;',
        '      const style = window.getComputedStyle(element);',
        '      if (!style || style.display === "none" || style.visibility === "hidden" || style.position === "fixed") return;',
        '      const rect = element.getBoundingClientRect();',
        '      if (!Number.isFinite(rect.bottom) || !Number.isFinite(bodyRect.top)) return;',
        '      const marginBottom = Number.parseFloat(style.marginBottom || "0") || 0;',
        '      contentHeight = Math.max(contentHeight, rect.bottom - bodyRect.top + marginBottom);',
        '    });',
        '    const bodyStyle = window.getComputedStyle(body);',
        '    const paddingBottom = Number.parseFloat(bodyStyle.paddingBottom || "0") || 0;',
        '    return Math.max(0, Math.ceil(contentHeight + paddingBottom));',
        '  }',
        '  function postHeight() {',
        '    scheduled = false;',
        '    const body = document.body;',
        '    const html = document.documentElement;',
        '    if (!body || !html || !window.parent) return;',
        '    const intrinsicHeight = measureIntrinsicHeight();',
        '    const fallbackHeight = Math.max(',
        '      body.scrollHeight || 0,',
        '      body.offsetHeight || 0,',
        '      html.scrollHeight || 0,',
        '      html.offsetHeight || 0',
        '    );',
        '    const height = intrinsicHeight > 0 ? intrinsicHeight : fallbackHeight;',
        '    if (!Number.isFinite(height) || height <= 0) return;',
        "    window.parent.postMessage({ channel: CHANNEL, type: 'height', runtimeId: RUNTIME_ID, height: Math.ceil(height) }, '*');",
        '  }',
        '  function schedulePostHeight() {',
        '    if (scheduled) return;',
        '    scheduled = true;',
        '    if (typeof window.requestAnimationFrame === "function") {',
        '      window.requestAnimationFrame(postHeight);',
        '      return;',
        '    }',
        '    setTimeout(postHeight, 16);',
        '  }',
        '  function observeDocument() {',
        '    if (typeof ResizeObserver === "function") {',
        '      const resizeObserver = new ResizeObserver(schedulePostHeight);',
        '      if (document.documentElement) resizeObserver.observe(document.documentElement);',
        '      if (document.body) resizeObserver.observe(document.body);',
        '    }',
        '    if (typeof MutationObserver === "function" && document.documentElement) {',
        '      const mutationObserver = new MutationObserver(schedulePostHeight);',
        '      mutationObserver.observe(document.documentElement, {',
        '        subtree: true,',
        '        childList: true,',
        '        characterData: true,',
        '        attributes: true,',
        '      });',
        '    }',
        '    document.addEventListener("toggle", schedulePostHeight, true);',
        '  }',
        '  if (document.readyState === "loading") {',
        '    document.addEventListener("DOMContentLoaded", function () {',
        '      observeDocument();',
        '      schedulePostHeight();',
        '    }, { once: true });',
        '  } else {',
        '    observeDocument();',
        '    schedulePostHeight();',
        '  }',
        '  window.addEventListener("load", function () {',
        '    schedulePostHeight();',
        '    setTimeout(schedulePostHeight, 80);',
        '    setTimeout(schedulePostHeight, 320);',
        '  });',
        "  window.addEventListener('message', function (event) {",
        '    const data = event && event.data ? event.data : null;',
        '    if (!data || data.channel !== CHANNEL || data.runtimeId !== RUNTIME_ID || data.type !== "viewport") return;',
        '    schedulePostHeight();',
        '  });',
        '  schedulePostHeight();',
        '})();',
    ].join('\n');
}
