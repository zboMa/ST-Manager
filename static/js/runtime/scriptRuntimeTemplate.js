export const SCRIPT_RUNTIME_CHANNEL = 'st-manager:script-runtime';

function escapeForInlineScript(value) {
    return JSON.stringify(value)
        .replace(/<\/script>/gi, '<\\/script>')
        .replace(/\u2028/g, '\\u2028')
        .replace(/\u2029/g, '\\u2029');
}

function unwrapScriptContent(content) {
    const source = String(content || '');
    const fenced = source.match(/^\s*```[^\n]*\n([\s\S]*?)\n```\s*$/i);
    return fenced ? fenced[1] : source;
}

function buildBootstrapScript(runtimeId, scriptSnapshot) {
    const stateLiteral = escapeForInlineScript({
        runtimeId,
        scriptId: String(scriptSnapshot.id || ''),
        name: String(scriptSnapshot.name || ''),
        info: String(scriptSnapshot.info || ''),
        data: scriptSnapshot.data || {},
        button: {
            enabled: Boolean(scriptSnapshot.button?.enabled),
            buttons: Array.isArray(scriptSnapshot.button?.buttons) ? scriptSnapshot.button.buttons : [],
        },
    });

    return [
        '(function () {',
        `  const CHANNEL = ${escapeForInlineScript(SCRIPT_RUNTIME_CHANNEL)};`,
        `  const INITIAL_STATE = ${stateLiteral};`,
        '  const root = document.documentElement;',
        '  const runtimeRoot = document.getElementById("st-manager-script-root");',
        '  const listeners = new Map();',
        '  const onceWrappers = new Map();',
        '  const pendingRequests = new Map();',
        '  let requestSequence = 0;',
        '  let state = cloneValue(INITIAL_STATE);',
        '',
        '  function cloneValue(value) {',
        '    if (typeof structuredClone === "function") {',
        '      try {',
        '        return structuredClone(value);',
        '      } catch (error) {',
        '      }',
        '    }',
        '    try {',
        '      return JSON.parse(JSON.stringify(value));',
        '    } catch (error) {',
        '      return value;',
        '    }',
        '  }',
        '',
        '  function normalizeButtons(buttons) {',
        '    if (!Array.isArray(buttons)) return [];',
        '    return buttons',
        '      .map(button => {',
        '        if (typeof button === "string") {',
        '          return { name: button, visible: true };',
        '        }',
        '        if (!button || typeof button !== "object") return null;',
        '        return {',
        '          name: String(button.name || "").trim(),',
        '          visible: button.visible !== false,',
        '        };',
        '      })',
        '      .filter(button => button && button.name);',
        '  }',
        '',
        '  function formatValue(value) {',
        '    if (value instanceof Error) {',
        '      return value.stack || `${value.name}: ${value.message}`;',
        '    }',
        '    if (typeof value === "string") {',
        '      return value;',
        '    }',
        '    if (typeof value === "undefined") {',
        '      return "undefined";',
        '    }',
        '    try {',
        '      if (typeof value === "object" && value !== null) {',
        '        return JSON.stringify(value, null, 2);',
        '      }',
        '      return String(value);',
        '    } catch (error) {',
        '      return Object.prototype.toString.call(value);',
        '    }',
        '  }',
        '',
        '  function serializeError(error) {',
        '    if (!error) {',
        '      return { name: "Error", message: "Unknown error", stack: "" };',
        '    }',
        '    if (typeof error === "string") {',
        '      return { name: "Error", message: error, stack: error };',
        '    }',
        '    return {',
        '      name: error.name || "Error",',
        '      message: error.message || String(error),',
        '      stack: error.stack || `${error.name || "Error"}: ${error.message || String(error)}`',
        '    };',
        '  }',
        '',
        '  function postToHost(type, payload) {',
        '    if (!window.parent) return;',
        '    window.parent.postMessage({',
        '      channel: CHANNEL,',
        '      runtimeId: state.runtimeId,',
        '      type,',
        '      payload: cloneValue(payload),',
        '    }, "*");',
        '  }',
        '',
        '  function applyViewportHeight(height) {',
        '    if (!Number.isFinite(height) || height <= 0) return;',
        '    root.style.setProperty("--stm-script-viewport-height", `${height}px`);',
        '  }',
        '',
        '  function measureIntrinsicHeight() {',
        '    const body = document.body;',
        '    if (!body) return 0;',
        '    let contentHeight = 0;',
        '    const bodyRect = body.getBoundingClientRect();',
        '    const elements = [body, ...Array.from(body.querySelectorAll("*"))];',
        '    elements.forEach((element) => {',
        '      if (!(element instanceof Element)) return;',
        '      const style = window.getComputedStyle(element);',
        '      if (!style || style.display === "none" || style.visibility === "hidden" || style.position === "fixed") return;',
        '      const rect = element.getBoundingClientRect();',
        '      if (!Number.isFinite(rect.bottom) || !Number.isFinite(bodyRect.top)) return;',
        '      const marginBottom = Number.parseFloat(style.marginBottom || "0") || 0;',
        '      contentHeight = Math.max(contentHeight, rect.bottom - bodyRect.top + marginBottom);',
        '    });',
        '    const bodyStyle = window.getComputedStyle(body);',
        '    const paddingBottom = Number.parseFloat(bodyStyle.paddingBottom || "0") || 0;',
        '    return Math.max(0, Math.ceil(contentHeight + paddingBottom));',
        '  }',
        '',
        '  function measureHeight() {',
        '    const body = document.body;',
        '    const html = document.documentElement;',
        '    if (!body || !html) return;',
        '    const intrinsicHeight = measureIntrinsicHeight();',
        '    const fallbackHeight = Math.max(',
        '      body.scrollHeight || 0,',
        '      body.offsetHeight || 0,',
        '      html.scrollHeight || 0,',
        '      html.offsetHeight || 0',
        '    );',
        '    const height = intrinsicHeight > 0 ? intrinsicHeight : fallbackHeight;',
        '    if (!Number.isFinite(height) || height <= 0) return;',
        '    postToHost("height", { height: Math.ceil(height) });',
        '  }',
        '',
        '  const scheduleMeasure = (() => {',
        '    let scheduled = false;',
        '    return function () {',
        '      if (scheduled) return;',
        '      scheduled = true;',
        '      const runner = () => {',
        '        scheduled = false;',
        '        measureHeight();',
        '      };',
        '      if (typeof window.requestAnimationFrame === "function") {',
        '        window.requestAnimationFrame(runner);',
        '      } else {',
        '        setTimeout(runner, 16);',
        '      }',
        '    };',
        '  })();',
        '',
        '  function observeDocument() {',
        '    if (typeof ResizeObserver === "function") {',
        '      const resizeObserver = new ResizeObserver(scheduleMeasure);',
        '      resizeObserver.observe(document.documentElement);',
        '      if (document.body) resizeObserver.observe(document.body);',
        '    }',
        '    if (typeof MutationObserver === "function") {',
        '      const mutationObserver = new MutationObserver(scheduleMeasure);',
        '      mutationObserver.observe(document.documentElement, {',
        '        subtree: true,',
        '        childList: true,',
        '        characterData: true,',
        '        attributes: true,',
        '      });',
        '    }',
        '    document.addEventListener("toggle", scheduleMeasure, true);',
        '  }',
        '',
        '  function emitLocal(eventName, detail) {',
        '    const handlers = listeners.get(eventName);',
        '    if (!handlers || handlers.size === 0) return;',
        '    Array.from(handlers).forEach(handler => {',
        '      try {',
        '        handler(detail);',
        '      } catch (error) {',
        '        console.error(error);',
        '      }',
        '    });',
        '  }',
        '',
        '  function on(eventName, handler) {',
        '    if (typeof handler !== "function") {',
        '      throw new TypeError("ManagerBridge.on requires a function handler");',
        '    }',
        '    if (!listeners.has(eventName)) {',
        '      listeners.set(eventName, new Set());',
        '    }',
        '    listeners.get(eventName).add(handler);',
        '    return () => off(eventName, handler);',
        '  }',
        '',
        '  function once(eventName, handler) {',
        '    const wrapper = detail => {',
        '      off(eventName, wrapper);',
        '      handler(detail);',
        '    };',
        '    onceWrappers.set(handler, wrapper);',
        '    return on(eventName, wrapper);',
        '  }',
        '',
        '  function off(eventName, handler) {',
        '    const handlers = listeners.get(eventName);',
        '    if (!handlers) return;',
        '    handlers.delete(handler);',
        '    const onceWrapper = onceWrappers.get(handler);',
        '    if (onceWrapper) {',
        '      handlers.delete(onceWrapper);',
        '      onceWrappers.delete(handler);',
        '    }',
        '    if (handlers.size === 0) {',
        '      listeners.delete(eventName);',
        '    }',
        '  }',
        '',
        '  function request(action, payload, options) {',
        '    const requestId = `${state.runtimeId}:${++requestSequence}`;',
        '    const timeoutMs = Math.max(1000, Math.min(30000, Number(options && options.timeout) || 10000));',
        '    return new Promise((resolve, reject) => {',
        '      const timeoutId = setTimeout(() => {',
        '        pendingRequests.delete(requestId);',
        '        reject(new Error(`Host request timed out: ${String(action || "unknown")}`));',
        '      }, timeoutMs);',
        '      pendingRequests.set(requestId, { resolve, reject, timeoutId });',
        '      postToHost("request", {',
        '        requestId,',
        '        action: String(action || ""),',
        '        payload: cloneValue(payload || {}),',
        '      });',
        '    });',
        '  }',
        '',
        '  function updateData(nextData, options) {',
        '    state.data = cloneValue(nextData || {});',
        '    if (!options || options.silent !== true) {',
        '      postToHost("sync-data", { data: state.data });',
        '    }',
        '    emitLocal("data:change", cloneValue(state.data));',
        '    emitLocal("context:update", cloneValue(getContext()));',
        '    scheduleMeasure();',
        '    return cloneValue(state.data);',
        '  }',
        '',
        '  function updateButtonConfig(nextButtonConfig, options) {',
        '    state.button = {',
        '      enabled: nextButtonConfig && nextButtonConfig.enabled !== false,',
        '      buttons: normalizeButtons(nextButtonConfig ? nextButtonConfig.buttons : []),',
        '    };',
        '    if (!options || options.silent !== true) {',
        '      postToHost("sync-button-config", { button: state.button });',
        '    }',
        '    emitLocal("buttons:change", cloneValue(state.button));',
        '    emitLocal("context:update", cloneValue(getContext()));',
        '    scheduleMeasure();',
        '    return cloneValue(state.button);',
        '  }',
        '',
        '  function getContext() {',
        '    return {',
        '      runtimeId: state.runtimeId,',
        '      scriptId: state.scriptId,',
        '      name: state.name,',
        '      info: state.info,',
        '      data: cloneValue(state.data),',
        '      button: cloneValue(state.button),',
        '    };',
        '  }',
        '',
        '  const originalConsole = {',
        '    log: console.log.bind(console),',
        '    debug: console.debug.bind(console),',
        '    info: console.info.bind(console),',
        '    warn: console.warn.bind(console),',
        '    error: console.error.bind(console),',
        '  };',
        '',
        '  ["log", "debug", "info", "warn", "error"].forEach(level => {',
        '    console[level] = (...args) => {',
        '      postToHost("log", {',
        '        level,',
        '        message: args.map(formatValue).join(" "),',
        '        timestamp: Date.now(),',
        '      });',
        '      originalConsole[level](...args);',
        '    };',
        '  });',
        '',
        '  window.addEventListener("error", event => {',
        '    const serialized = serializeError(event.error || event.message || "Script runtime error");',
        '    postToHost("status", { status: "error", detail: serialized });',
        '    postToHost("log", { level: "error", message: serialized.stack || serialized.message, timestamp: Date.now() });',
        '  });',
        '',
        '  window.addEventListener("unhandledrejection", event => {',
        '    const serialized = serializeError(event.reason || "Unhandled promise rejection");',
        '    postToHost("status", { status: "error", detail: serialized });',
        '    postToHost("log", { level: "error", message: serialized.stack || serialized.message, timestamp: Date.now() });',
        '  });',
        '',
        '  window.addEventListener("message", event => {',
        '    const message = event && event.data ? event.data : null;',
        '    if (!message || message.channel !== CHANNEL || message.runtimeId !== state.runtimeId) {',
        '      return;',
        '    }',
        '    if (message.type === "response") {',
        '      const pending = pendingRequests.get(String(message.requestId || ""));',
        '      if (!pending) return;',
        '      clearTimeout(pending.timeoutId);',
        '      pendingRequests.delete(String(message.requestId || ""));',
        '      if (message.ok === false) {',
        '        const normalized = serializeError(message.error || "Host request failed");',
        '        const error = new Error(normalized.message);',
        '        error.name = normalized.name;',
        '        if (normalized.stack) {',
        '          error.stack = normalized.stack;',
        '        }',
        '        pending.reject(error);',
        '      } else {',
        '        pending.resolve(cloneValue(message.result));',
        '      }',
        '      return;',
        '    }',
        '    if (message.type === "viewport") {',
        '      applyViewportHeight(Number(message.payload && message.payload.height));',
        '      scheduleMeasure();',
        '      return;',
        '    }',
        '    if (message.type === "host-sync") {',
        '      const payload = message.payload || {};',
        '      if (Object.prototype.hasOwnProperty.call(payload, "name")) state.name = String(payload.name || "");',
        '      if (Object.prototype.hasOwnProperty.call(payload, "info")) state.info = String(payload.info || "");',
        '      if (Object.prototype.hasOwnProperty.call(payload, "data")) updateData(payload.data, { silent: true });',
        '      if (Object.prototype.hasOwnProperty.call(payload, "button")) updateButtonConfig(payload.button, { silent: true });',
        '      emitLocal("host:sync", cloneValue(payload));',
        '      return;',
        '    }',
        '    if (message.type === "host-event") {',
        '      const payload = cloneValue(message.payload || {});',
        '      emitLocal(payload.event || "host:event", payload);',
        '      if (payload.event === "button" && payload.name) {',
        '        emitLocal(`button:${payload.name}`, payload);',
        '        emitLocal("button", payload);',
        '      }',
        '    }',
        '  });',
        '',
        '  function showToast(message, duration) {',
        '    return request("toast", {',
        '      message: String(message || ""),',
        '      duration: Number.isFinite(Number(duration)) ? Number(duration) : undefined,',
        '    });',
        '  }',
        '',
        '  function setText(target, value) {',
        '    if (!target) return null;',
        '    target.textContent = String(value == null ? "" : value);',
        '    scheduleMeasure();',
        '    return target;',
        '  }',
        '',
        '  function append(target, child) {',
        '    if (!target) return null;',
        '    if (typeof child === "string") {',
        '      target.insertAdjacentHTML("beforeend", child);',
        '    } else if (child instanceof Node) {',
        '      target.appendChild(child);',
        '    }',
        '    scheduleMeasure();',
        '    return target;',
        '  }',
        '',
        '  function createElement(tagName, attrs) {',
        '    const element = document.createElement(tagName);',
        '    if (attrs && typeof attrs === "object") {',
        '      Object.entries(attrs).forEach(([key, value]) => {',
        '        if (value == null) return;',
        '        if (key === "text") {',
        '          element.textContent = String(value);',
        '          return;',
        '        }',
        '        if (key === "html") {',
        '          element.innerHTML = String(value);',
        '          return;',
        '        }',
        '        if (key === "style" && typeof value === "object") {',
        '          Object.assign(element.style, value);',
        '          return;',
        '        }',
        '        if (key in element) {',
        '          try {',
        '            element[key] = value;',
        '            return;',
        '          } catch (error) {',
        '          }',
        '        }',
        '        element.setAttribute(key, String(value));',
        '      });',
        '    }',
        '    return element;',
        '  }',
        '',
        '  const ManagerBridge = {',
        '    channel: CHANNEL,',
        '    runtimeId: state.runtimeId,',
        '    getRoot: () => runtimeRoot,',
        '    render: html => {',
        '      runtimeRoot.innerHTML = String(html || "");',
        '      scheduleMeasure();',
        '      return runtimeRoot;',
        '    },',
        '    clear: () => {',
        '      runtimeRoot.innerHTML = "";',
        '      scheduleMeasure();',
        '    },',
        '    getContext: () => cloneValue(getContext()),',
        '    getData: () => cloneValue(state.data),',
        '    setData: value => updateData(value),',
        '    patchData: patch => updateData({ ...(state.data || {}), ...(patch || {}) }),',
        '    getButtonConfig: () => cloneValue(state.button),',
        '    getButtons: () => cloneValue(state.button.buttons),',
        '    setButtons: buttons => updateButtonConfig({ enabled: state.button.enabled, buttons }),',
        '    setButtonEnabled: enabled => updateButtonConfig({ enabled: Boolean(enabled), buttons: state.button.buttons }),',
        '    setButtonConfig: button => updateButtonConfig(button),',
        '    createElement,',
        '    setText,',
        '    append,',
        '    request,',
        '    fetch: (url, options) => request("fetch", { url, ...(options || {}) }),',
        '    fetchText: (url, options) => request("fetch", { url, ...(options || {}), responseType: "text" }),',
        '    fetchJson: (url, options) => request("fetch", { url, ...(options || {}), responseType: "json" }),',
        '    getHostState: () => request("get-host-state"),',
        '    getActiveContext: () => request("get-active-context"),',
        '    getActiveCard: () => request("get-active-card"),',
        '    getActivePreset: () => request("get-active-preset"),',
        '    getActiveChat: () => request("get-active-chat"),',
        '    listRuntimes: () => request("list-runtimes"),',
        '    getRuntimeState: () => request("get-runtime-state"),',
        '    openDetail: (target, id) => request("open-detail", { target, id }),',
        '    refreshList: target => request("refresh-list", { target }),',
        '    reloadRuntime: () => request("reload-runtime"),',
        '    stopRuntime: () => request("stop-runtime"),',
        '    showToast,',
        '    on,',
        '    once,',
        '    off,',
        '    emit: (eventName, detail) => {',
        '      postToHost("event", { eventName: String(eventName || ""), detail: cloneValue(detail) });',
        '      emitLocal(String(eventName || ""), cloneValue(detail));',
        '    },',
        '    onButtonClick: (name, handler) => {',
        '      if (typeof name === "function") {',
        '        return on("button", payload => name(payload && payload.name, payload));',
        '      }',
        '      return on(`button:${String(name || "")}`, payload => handler(payload));',
        '    },',
        '  };',
        '',
        '  Object.defineProperty(window, "ManagerBridge", { value: ManagerBridge, configurable: false, writable: false });',
        '  window.showToast = showToast;',
        '  window.getScriptData = ManagerBridge.getData;',
        '  window.setScriptData = ManagerBridge.setData;',
        '  window.patchScriptData = ManagerBridge.patchData;',
        '  window.getScriptButtons = ManagerBridge.getButtons;',
        '  window.setScriptButtons = ManagerBridge.setButtons;',
        '  window.setScriptButtonConfig = ManagerBridge.setButtonConfig;',
        '  window.onScriptEvent = ManagerBridge.on;',
        '  window.emitScriptEvent = ManagerBridge.emit;',
        '  window.onButtonClick = ManagerBridge.onButtonClick;',
        '  emitLocal("__bridge_ready__", { capabilities: [',
        '    "toast",',
        '    "fetch(text/json)",',
        '    "get-host-state",',
        '    "get-active-context/card/preset/chat",',
        '    "list-runtimes",',
        '    "open-detail",',
        '    "refresh-list",',
        '    "get-runtime-state",',
        '    "reload-runtime",',
        '    "stop-runtime"',
        '  ] });',
        '  postToHost("event", { eventName: "__bridge_ready__", detail: { capabilities: [',
        '    "toast",',
        '    "fetch(text/json)",',
        '    "get-host-state",',
        '    "get-active-context/card/preset/chat",',
        '    "list-runtimes",',
        '    "open-detail",',
        '    "refresh-list",',
        '    "get-runtime-state",',
        '    "reload-runtime",',
        '    "stop-runtime"',
        '  ] } });',
        '',
        '  document.addEventListener("DOMContentLoaded", () => {',
        '    observeDocument();',
        '    scheduleMeasure();',
        '  }, { once: true });',
        '  window.addEventListener("load", () => {',
        '    scheduleMeasure();',
        '    setTimeout(scheduleMeasure, 80);',
        '    setTimeout(scheduleMeasure, 320);',
        '  });',
        '',
        '  applyViewportHeight(window.innerHeight || 0);',
        '  postToHost("ready", cloneValue(getContext()));',
        '  postToHost("status", { status: "running" });',
        '})();',
    ].join('\n');
}

export function buildScriptRuntimeDocument(runtimeId, scriptSnapshot) {
    const bootstrapScript = buildBootstrapScript(runtimeId, scriptSnapshot).replace(/<\/script>/gi, '<\\/script>');
    const userScript = unwrapScriptContent(scriptSnapshot.content).replace(/<\/script>/gi, '<\\/script>');

    return `<!DOCTYPE html>
<html>
<head>
<meta charset="utf-8">
<meta name="viewport" content="width=device-width, initial-scale=1.0">
<base href="${window.location.origin}/">
<style>
  :root {
    --stm-script-viewport-height: 100vh;
    color-scheme: dark;
  }
  *, *::before, *::after {
    box-sizing: border-box;
  }
  html, body {
    width: 100%;
    min-height: auto !important;
    height: auto !important;
    margin: 0 !important;
    padding: 0 !important;
    overflow-x: hidden !important;
    overflow-y: auto !important;
    background: transparent !important;
    color: #e5e7eb;
    font-family: ui-sans-serif, system-ui, -apple-system, BlinkMacSystemFont, "Segoe UI", sans-serif;
  }
  body {
    padding: 0;
  }
  #st-manager-script-root {
    display: block;
    width: 100%;
    min-height: 0;
  }
  img, canvas, svg, video {
    max-width: 100%;
  }
</style>
<script>${bootstrapScript}</script>
</head>
<body>
  <div id="st-manager-script-root"></div>
  <script type="module">${userScript}</script>
</body>
</html>`;
}
